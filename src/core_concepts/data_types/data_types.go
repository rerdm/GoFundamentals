package data_types

import "fmt"

func PrintDataTypes() {
    // Integer
    var i int = 42
    // Float
    var f float64 = 3.14
    // String
    var s string = "Hello, Go!"
    // Boolean
    var b bool = true

    fmt.Println("Integer:", i)
    fmt.Println("Float:", f)
    fmt.Println("String:", s)
    fmt.Println("Boolean:", b)
}
